package modulo_1.tiposVars;

public class Main {

	public static void main(String[] args) {
		//  Sintaxe:
		// <tipo> <nome> = <valor>
		int idade = 19;
		double altura = 1.88;
		char sexo = 'M';
		
		// Uma variável possui:
		// Nome
		// Tipo
		// Valor
		// Endereço ** 
		
		System.out.println("Nome: João Victor");
		System.out.println("Idade: " + idade);
		System.out.println("Altura: " + altura);
		System.out.println("Sexo: " + sexo);
	}

}
